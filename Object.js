class Dayo{
    constructor(name,age,size) {
        this.name = name;
        this.age = age;
        this.size = size;
    }

    setName(newName){
        this.name = newName;
    }

    getName(){
        return this.name;
    }
    setAge(newAge){
        this.age = newAge;
    }
    getAge(){
        return this.age;
    }

    setSize(newSize){
        this.size = newSize;
    }
    getSize(){
        return this.size;
    }
}